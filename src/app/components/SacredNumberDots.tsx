import React from 'react';

interface SacredNumberDotsProps {
  number: number;
  size?: 'sm' | 'md' | 'lg';
}

export function SacredNumberDots({ number, size = 'md' }: SacredNumberDotsProps) {
  const dotSizes = {
    sm: 'w-2 h-2',
    md: 'w-3 h-3',
    lg: 'w-4 h-4'
  };

  const arrangements: { [key: number]: number[][] } = {
    1: [[0, 0]],
    2: [[-1, 0], [1, 0]],
    3: [[-1.2, -1.2], [0, 0], [1.2, 1.2]],
    4: [[-1.2, -1.2], [1.2, -1.2], [-1.2, 1.2], [1.2, 1.2]],
    5: [[-1.2, -1.2], [1.2, -1.2], [0, 0], [-1.2, 1.2], [1.2, 1.2]],
    6: [[-1.2, -1.8], [-1.2, 0], [-1.2, 1.8], [1.2, -1.8], [1.2, 0], [1.2, 1.8]],
    7: [[-1.2, -1.8], [-1.2, 0], [-1.2, 1.8], [0, 0], [1.2, -1.8], [1.2, 0], [1.2, 1.8]],
    8: [[-1.5, -1.8], [-1.5, -0.6], [-1.5, 0.6], [-1.5, 1.8], [1.5, -1.8], [1.5, -0.6], [1.5, 0.6], [1.5, 1.8]],
    9: [[-1.5, -1.8], [-1.5, 0], [-1.5, 1.8], [0, -1.8], [0, 0], [0, 1.8], [1.5, -1.8], [1.5, 0], [1.5, 1.8]],
    10: [[-1.5, -2.2], [-1.5, -1], [-1.5, 0.2], [-1.5, 1.4], [0, -2.2], [0, -1], [0, 0.2], [0, 1.4], [1.5, -2.2], [1.5, -1]],
    11: [[-1.8, -2.2], [-1.8, -1], [-1.8, 0.2], [-1.8, 1.4], [0, -2.2], [0, -1], [0, 0.2], [0, 1.4], [0, 2.6], [1.8, -2.2], [1.8, -1]],
    12: [[-1.8, -2.2], [-1.8, -1], [-1.8, 0.2], [-1.8, 1.4], [0, -2.2], [0, -1], [0, 0.2], [0, 1.4], [0, 2.6], [1.8, -2.2], [1.8, -1], [1.8, 0.2]],
    13: [[-1.8, -2.2], [-1.8, -1], [-1.8, 0.2], [-1.8, 1.4], [0, -2.2], [0, -1], [0, 0.2], [0, 1.4], [0, 2.6], [1.8, -2.2], [1.8, -1], [1.8, 0.2], [1.8, 1.4]]
  };

  const positions = arrangements[number] || arrangements[1];
  const spacing = size === 'lg' ? 12 : size === 'md' ? 10 : 8;

  return (
    <div className="flex items-center justify-center py-4">
      <div className="relative" style={{ width: '120px', height: '120px' }}>
        {positions.map((pos, idx) => (
          <div
            key={idx}
            className={`absolute ${dotSizes[size]} bg-amber-700 rounded-full transform -translate-x-1/2 -translate-y-1/2`}
            style={{
              left: `${50 + pos[0] * spacing}%`,
              top: `${50 + pos[1] * spacing}%`,
            }}
          />
        ))}
      </div>
    </div>
  );
}